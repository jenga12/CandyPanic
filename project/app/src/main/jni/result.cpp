/*
 * クラス名：CResult
* 関数名：Init()
* 内容：初期化処理
*/
int CResult :: Init(void){
	Vec2 size(256.0f, 128.0f);
	Vec2 pos(640.0f, 360.0f);
	m_pTestSprite = C2DSprite :: Create("result.img", &size, 1);
	m_pTestSprite->SetPosition(&pos);
	m_pTestSprite->LinkList(OBJECT_2D_UNKNOWN);
	return 0;
}

/*
 * クラス名：CResult
 * 関数名：Final()
 * 内容：終了処理
 */
void CResult :: Final(void){
	m_pTestSprite->Release();
}

/*
 * クラス名：CResult
 * 関数名：Update()
 * 内容：更新処理
 */
void CResult :: Update(void){
	const INPUT *pInput = CMainManager :: GetInput();

	if(pInput[0].flag == 1){
		CStateManager :: SetNextState(new CTitle());
	}
}

